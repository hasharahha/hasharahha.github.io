function loadLayerA(){
  for(var y=0; y<layerA.length; y++){
    for(var x=0; x<layerA[y].length; x++){
      try{
        if(layerA[y][x] ==0){
          ctx.drawImage(tiles, 0, 0, 50, 50, x*50, y*50, 50, 50);
        }
        if(layerA[y][x] ==1){
          ctx.drawImage(tiles, 50, 0, 50, 50, x*50, y*50, 50, 50);
        }
      }catch(e){
        
      }
    }
  }
  window.requestAnimationFrame(function(){
    loadLayerA();
  }, 1000/30);
}

